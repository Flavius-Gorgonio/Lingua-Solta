void moduloProfessor(void) {
    char opcao;
    do {
        opcao = telaMenuProfessor();
        switch(opcao) {
            case '1': 	telaCadastrarProfessor();
                        break;
            case '2': 	telaPesquisarProfessor();
                        break;
            case '3': 	telaAlterarProfessor();
                        break;
            case '4': 	telaExcluirProfessor();
                        break;
        } 		
    } while (opcao != '0');
}



